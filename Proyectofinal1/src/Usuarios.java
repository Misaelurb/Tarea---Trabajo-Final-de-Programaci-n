    void listar(){
        String SQL="Select * from registro";
        try {
            cn = con.getConnection();
            st=cn.createStatement();
            rs=st.executeQuery(SQL);
            Object[]persona=new Object[7];
            modelo=(DefaultTableModel)tabla.getModel();
            while (rs.next()){
                persona[0]=rs.getString("usuario");
                persona[1]=rs.getString("nombre");
                persona[2]=rs.getString("apellido");
                persona[3]=rs.getString("contraseña");
                persona[4]=rs.getString("contraseña2");
                persona[5]=rs.getString("correo");
                persona[6]=rs.getString("telefono");
                modelo.addRow(persona); 
            }
            tabla.setModel(modelo);
        } catch (Exception e) {
           
        }
    }
    
