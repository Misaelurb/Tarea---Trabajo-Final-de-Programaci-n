    private void tablaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaMouseClicked
        int fila=tabla.getSelectedRow();
        if(fila==-1){
            JOptionPane.showMessageDialog(null, "Usuario no seleccionado");
        }else {
            String usuario=(String)tabla.getValueAt(fila, 0);
            String nombre=(String)tabla.getValueAt(fila, 1);
            String apellido=(String)tabla.getValueAt(fila, 2);
            String contraseña=(String)tabla.getValueAt(fila, 3);
            String contraseña2=(String)tabla.getValueAt(fila, 4);
            String correo=(String)tabla.getValueAt(fila, 5);
            String telefono=(String)tabla.getValueAt(fila, 6);
            txtusuario.setText(usuario);
            txtnombre.setText(nombre);
            txtapellido.setText(apellido);
            txtcontraseña.setText(contraseña);
            txtcontraseña2.setText(contraseña2);
            txtcorreo.setText(correo);
            txttelefono.setText(telefono);
        }
    }//GEN-LAST:event_tablaMouseClicked
    void editar(){
        String usuario=txtusuario.getText();
        String nombre=txtnombre.getText();
        String apellido=txtapellido.getText();
        String contraseña=txtcontraseña.getText();
        String contraseña2=txtcontraseña2.getText();
        String correo=txtcorreo.getText();
        String telefono=txttelefono.getText();
        String sql="update registro set usuario='"+usuario+"',nombre='"+nombre+"',apellido='"+apellido+"',contraseña='"+contraseña+"',contraseña2='"+contraseña2+"',correo='"+correo+"' where telefono="+telefono;
        if(usuario.equals("")||nombre.equals("")||apellido.equals("")||contraseña.equals("")||contraseña2.equals("")||correo.equals("")||telefono.equals("")){
            JOptionPane.showMessageDialog(null, "Debe ingresar los datos");
        }else    {
            try {
                cn=con.getConnection();
                st=cn.createStatement();
                st.executeUpdate(sql);
                JOptionPane.showMessageDialog(null, "Usuario actualizado");
                limpiartabla();
            } catch (Exception e) {
            }
        }        
    }
    
